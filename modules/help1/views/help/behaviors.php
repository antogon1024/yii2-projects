<p>Поведения (behaviors) — это экземпляры класса <a href="#">yii\base\Behavior</a> или класса, унаследованного от него. Поведения,
    также известные как <a href="#">примеси</a>, позволяют расширять
    функциональность существующих <a href="#">компонентов</a> без необходимости изменения дерева наследования.
    После прикрепления поведения к компоненту, его методы и свойства "внедряются" в компонент, и становятся доступными
    так же, как если бы они были объявлены в самом классе компонента. Кроме того, поведение может реагировать на
    <a href="#">события</a>, создаваемые компонентом, что позволяет тонко настраивать или модифицировать
    обычное выполнение кода компонента.</p>

